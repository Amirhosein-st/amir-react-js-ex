function sum(num1 , num2) {
    return num1 + num2
}

function sub(num1 , num2) {
    return num1 - num2
}

function mul(num1 , num2) {
    return num1 * num2
}

function div(num1 , num2) {
    return num1 / num2
}

 console.log(`4 + 10 = ${sum(4 , 10)}`);
 console.log(`10 - 4 = ${sub(10 , 4)}`);
 console.log(`4 * 10 = ${mul(4 , 10)}`);
 console.log(`10 / 5 = ${div(10 , 5)}`);